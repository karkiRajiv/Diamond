package com.example.testingactivity1.Arthimetic

object Arithmetic {
        fun add(first:Float,Second:Float):Float{
            return (first+Second)
        }

        fun subtract(first: Float, Second: Float):Float{
            return (first-Second)
        }

        fun divide(first: Float, Second: Float):Float{
            return (first/Second)
        }
    }
